          <h3>The Problem with PD</h3>
          <p class="small">The current PD system for teachers is broken, offering meaningless accreditations and few inspirations. Often, it’s a money grab by companies that are more worried about keeping their lights on than serving our kids.  None of it leads them to real leadership. Little of it pays any attention to the basic research about effective PD. The format is broken, the teacher’s are bored. Teacher’s aren’t activated and celebrated as the leaders they are. </p>
